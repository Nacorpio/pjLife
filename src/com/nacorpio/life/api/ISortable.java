package com.nacorpio.life.api;

public interface ISortable {

	/**
	 * Returns the name of the livable.
	 * @return the name.
	 */
	public String getName();
	
	/**
	 * Returns the species of the livable.
	 * @return the species.
	 */
	public String getSpecies();
	
	/**
	 * Returns the order of the livable.
	 * @return the order.
	 */
	public String getOrder();
	
	/**
	 * Returns the genus of the livable.
	 * @return the genus.
	 */
	public String getGenus();
	
}
